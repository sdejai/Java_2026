package Lec1;

public class DataTypes {

	public static void main(String[] args) {
		byte byteNumber=123;
		System.out.println(byteNumber);
		short shortNumber=1234;
		System.out.println(shortNumber);
		int intNumber=123432;
		System.out.println(intNumber);
		long longNumber = 133252;
		System.out.println(longNumber);
		float floatNumber=123.43f;
		System.out.println(floatNumber);
		double doubleNumber=987654.242456d;
		System.out.println(doubleNumber);
		char character='a';
		System.out.println(character);
		boolean bool = true;
		System.out.println(bool);
		System.out.println(byteNumber +" "+ shortNumber +" "+intNumber +" "+longNumber+" "+floatNumber+" "+doubleNumber+" "+character+" "+bool);
	}

}
