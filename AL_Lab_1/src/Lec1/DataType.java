package Lec1;

public class DataType {

	public static void main(String[] args) {
		byte byteNumber =12;
		System.out.println(byteNumber);
		short shortNumber =13112;
		System.out.println(shortNumber);
		int intNumber =14223;
		System.out.println(intNumber);
		long longNumber =122245;
		System.out.println(longNumber);
		float floatNumber =13423.232f;
		System.out.println(floatNumber);
		double doubleNumber =12342.6543d;
		System.out.println(doubleNumber);
		char character ='a';
		System.out.println(character);
		boolean bool =true;
		System.out.println(bool);
		System.out.println(byteNumber+ " "+shortNumber+" " +intNumber +" "+character+" " +bool +" "+longNumber +" "+floatNumber +" "+doubleNumber);
	}

}
